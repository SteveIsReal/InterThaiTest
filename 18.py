x = int(input())
if x < 0:
    print('Error : Please input positive number')
elif x > 9 or x == 0:
    print('Error : Out of range')
else:
    if x == 1:print('I')
    if x == 2:print('II')
    if x == 3:print('III')
    if x == 4:print('IV')
    if x == 5:print('V')
    if x == 6:print('VI')
    if x == 7:print('VII')
    if x == 8:print('VIII')
    if x == 9:print('IX')